<?php


	//短路
	//逻辑与        一边为假  即为假 
	//逻辑或        一边为真  即为真
	


	 //出现短路
	 //逻辑与出现短路  左边为假  会短路
	 //逻辑或出现短路  左边为真  会短路
	 

	 // 逻辑与短路
	 //后面表达式不执行
	 $a = 10;
	 //0 本身就是false
	 var_dump(($b=0) && ($a=100));
	 echo $a;
	 echo '<br/>';
	 echo $b;

	 echo '<hr/>';

	 //逻辑或短路
	 //后面表达式不执行
	 
	 $a = 10;
	 // 记住这句话  非0即真

	 var_dump(($b=10) || ($a=10000000));
	 echo $a;
	 echo '<br/>';
	 echo $b;

	 if(($b=0) || ($a=19999)){
	 	echo '111';
	 	echo $a;
	 }else{
	 	echo '2222';
	 	echo $a;
	 }




